package com.jiuy.core.meta.jiucoinsetting;
public class ContinuousSetting{
	private int days;
	private int extraJiuCoin;
	
	public ContinuousSetting(){}
	
	public int getDays() {
		return days;
	}
	public void setDays(int days) {
		this.days = days;
	}
	public int getExtraJiuCoin() {
		return extraJiuCoin;
	}
	public void setExtraJiuCoin(int extraJiuCoin) {
		this.extraJiuCoin = extraJiuCoin;
	}
}