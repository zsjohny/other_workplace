package com.jiuy.core.meta.clickstatistics;

public class ClickStatisticsVO extends ClickStatistics {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 6983062083799421888L;

	private String floorId;
	
	private String floorName;
	
	private String templateId;
	
	private String serialNumber;
	
	private String elementId;
	
	private String templateImgUrl;

	public String getFloorId() {
		return floorId;
	}

	public void setFloorId(String floorId) {
		this.floorId = floorId;
	}

	public String getFloorName() {
		return floorName;
	}

	public void setFloorName(String floorName) {
		this.floorName = floorName;
	}

	public String getTemplateId() {
		return templateId;
	}

	public void setTemplateId(String templateId) {
		this.templateId = templateId;
	}

	public String getSerialNumber() {
		return serialNumber;
	}

	public void setSerialNumber(String serialNumber) {
		this.serialNumber = serialNumber;
	}

	public String getElementId() {
		return elementId;
	}

	public void setElementId(String elementId) {
		this.elementId = elementId;
	}

	public String getTemplateImgUrl() {
		return templateImgUrl;
	}

	public void setTemplateImgUrl(String templateImgUrl) {
		this.templateImgUrl = templateImgUrl;
	}
	
	
}
